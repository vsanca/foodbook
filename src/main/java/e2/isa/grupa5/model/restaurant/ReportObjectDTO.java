package e2.isa.grupa5.model.restaurant;

public class ReportObjectDTO {
	
	private String name;
	private int value;
	private int grade;
	
	public ReportObjectDTO() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}
	
}
